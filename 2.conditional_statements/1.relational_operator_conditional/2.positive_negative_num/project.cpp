// finding the given number is positive or negative

#include <iostream>
using namespace std;

int main(){

        int x;
        cout<<"Enter any number: ";
        cin>>x;

        if(x>0){
            cout<<x<<" is positive number."<<endl;

        }
        else{
            cout<<x<<" is negative number."<<endl;
        }

        return 0;
}
